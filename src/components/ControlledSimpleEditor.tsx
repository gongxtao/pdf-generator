// 定义组件的props接口
interface ControlledSimpleEditorProps {
  value?: string // 编辑器的内容值
  onChange?: (content: string) => void // 内容变化时的回调函数
  height?: number // 编辑器高度（可选，不提供时自适应容器）
  placeholder?: string // 占位符文本
}

const MainToolbarContent = ({
  onHighlighterClick,
  onLinkClick,
  isMobile,
}: {
  onHighlighterClick: () => void
  onLinkClick: () => void
  isMobile: boolean
}) => {
  return (
    <>
      <ToolbarGroup>
        <UndoRedoButton action="undo" />
        <UndoRedoButton action="redo" />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <HeadingDropdownMenu />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <ListDropdownMenu />
        <BlockquoteButton />
        <CodeBlockButton />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <MarkButton type="bold" />
        <MarkButton type="italic" />
        <MarkButton type="strike" />
        <MarkButton type="code" />
        <MarkButton type="subscript" />
        <MarkButton type="superscript" />
        {isMobile ? (
          <Button data-style="ghost" onClick={onHighlighterClick}>
            <HighlighterIcon className="tiptap-button-icon" />
          </Button>
        ) : (
          <ColorHighlightPopover>
            <ColorHighlightPopoverButton />
            <ColorHighlightPopoverContent />
          </ColorHighlightPopover>
        )}
        {isMobile ? (
          <Button data-style="ghost" onClick={onLinkClick}>
            <LinkIcon className="tiptap-button-icon" />
          </Button>
        ) : (
          <LinkPopover />
        )}
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <TextAlignButton align="left" />
        <TextAlignButton align="center" />
        <TextAlignButton align="right" />
        <TextAlignButton align="justify" />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <ImageUploadButton />
      </ToolbarGroup>

      <Spacer />
    </>
  )
}

const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link"
  onBack: () => void
}) => (
  <>
    <ToolbarGroup>
      <Button data-style="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
)

export function ControlledSimpleEditor({
  value = '',
  onChange,
  height,
  placeholder = '开始输入内容...'
}: ControlledSimpleEditorProps) {
  const isMobile = useIsMobile()
  const { height: windowHeight } = useWindowSize()
  const [mobileView, setMobileView] = React.useState<
    "main" | "highlighter" | "link"
  >("main")
  const toolbarRef = React.useRef<HTMLDivElement>(null)

  const editor = useEditor({
    immediatelyRender: false,
    shouldRerenderOnTransaction: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": "Main content area, start typing to enter text.",
        class: "simple-editor",
      },
    },
    extensions: [
      StarterKit.configure({
        horizontalRule: false,
        link: {
          openOnClick: false,
          enableClickSelection: true,
        },
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,
      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: handleImageUpload,
        onError: (error) => console.error("Upload failed:", error),
      }),
    ],
    content: value, // 使用传入的value作为初始内容
    onUpdate: ({ editor }) => {
      // 当编辑器内容更新时，调用onChange回调
      const html = editor.getHTML()
      onChange?.(html)
    },
  })

  // 当外部value变化时，更新编辑器内容
  React.useEffect(() => {
    if (editor && value !== editor.getHTML()) {
      editor.commands.setContent(value)
    }
  }, [editor, value])

  const rect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  })

  React.useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main")
    }
  }, [isMobile, mobileView])

  // 动态计算样式 - 如果提供了height则使用固定高度，否则使用flex布局自适应
  const containerStyle = height 
    ? { height }
    : { display: 'flex', flexDirection: 'column' as const, height: '100%' }

  const contentStyle = height
    ? {
        height: `calc(${height}px - 60px)`, // 减去工具栏高度
        overflow: 'auto' as const
      }
    : {
        flex: 1,
        overflow: 'auto' as const,
        minHeight: '200px'
      }

  return (
    <div className="simple-editor-wrapper" style={containerStyle}>
      <EditorContext.Provider value={{ editor }}>
        <Toolbar
          ref={toolbarRef}
          style={{
            ...(isMobile
              ? {
                  bottom: `calc(100% - ${windowHeight - rect.y}px)`,
                }
              : {}),
          }}
        >
          {mobileView === "main" ? (
            <MainToolbarContent
              onHighlighterClick={() => setMobileView("highlighter")}
              onLinkClick={() => setMobileView("link")}
              isMobile={isMobile}
            />
          ) : (
            <MobileToolbarContent
              type={mobileView === "highlighter" ? "highlighter" : "link"}
              onBack={() => setMobileView("main")}
            />
          )}
        </Toolbar>

        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content"
          style={contentStyle}
        />
      </EditorContext.Provider>
    </div>
  )
}